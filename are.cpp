struct ci{
    int x,y,r;
};
    double PI = 2*acos(0.0);

double hypot( double l, double r ){
  return pow(l*l+r*r,0.5);
}
double area(ci A, ci B) {

   double d = hypot(B.x - A.x, B.y - A.y);

    if (d < A.r + B.r) {

       double a = A.r * A.r;
        double b = B.r * B.r;

       double x = (a - b + d * d) / (2 * d);
       double z = x * x;
       double y = sqrt(a - z);

        if (d <= abs(B.r - A.r)) {
            return PI * min(a, b);
        }
        return a * asin(y / A.r) + b * asin(y / B.r) - y * (x + sqrt(z + b - a));
    }
    return 0;
}
