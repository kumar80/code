string da(string &s){
    string a = "";
    for(int i=0;i<s.size();i++){
        if(s[i]>='0' && s[i]<='9')
            a+=s[i];
        else break;
    }
    s.erase(0, a.size()+3);
    //cout<<s;
    return a;
}
string mon(string &s){
    string a = "";
    for(int i=0;i<s.size();i++){
        if(s[i] == ' ')
            break; 
        a+=s[i];
    }
    s.erase(0,a.size()+1);
    if(a=="Jan") 
        return "01";
    if(a == "Feb")
        return "02";
    if(a == "Mar")
        return "03";
    if(a== "Apr")
        return "04";
    if(a== "May")
        return "05";
    if(a== "Jun")
        return "06";    
    if(a== "Jul")
        return "07";    
    if(a== "Aug")
        return "08";    
    if(a== "Sep")
        return "09";    
    if(a== "Oct")
        return "10";    
    if(a== "Nov")
        return "11";    
    return "12";
}
vector<string> play(vector<string>d){
    vector<string>ans;
    for(int i=0; i<d.size(); i++){
        string date = da(d[i]);
        string month = mon(d[i]);
        string year =  d[i];
        if(date.length() == 1) date  = '0' + date;
        ans.push_back(year + "-" + month + "-" + date);
    }
    return ans;

}
