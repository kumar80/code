const long long INF = 1e18;
const long long MAX = 2e5+10;
bool vis[500][500];  int n,m,x,y;
bool check(int u, int v) {
    if(u<=0 || v<=0 || u>n || v>m) return 0;
    if(vis[u][v]) return 0;
    vis[u][v]=1;
   // cout<<u<<" "<<v<<"\n";
    return 1;
}

int main(){
    fastio;
    int t=1; //cin>>t;
    while(t--){
        cin>>n>>m>>x>>y;
        memset(vis,false,sizeof(vis));
        queue<pair<int,int>> q;
        q.push({x,y});
        while(!q.empty()) {
            auto v = q.front() ; q.pop();
            vis[v.first][v.second] = 1; 
            cout<<v.first<<" "<<v.second<<"\n";
            if(check(v.first+1,v.second))  q.push({v.first+1,v.second});
            if(check(v.first-1,v.second))  q.push({v.first-1,v.second});
            if(check(v.first,v.second+1))  q.push({v.first,v.second+1});
            if(check(v.first,v.second-1))  q.push({v.first,v.second-1});
        }

    }
}