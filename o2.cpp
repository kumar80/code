const long long INF = 1e18;
const long long MAX = 2e5+10;
int n,q,m;
vector<pair<int,int>>adj[MAX];

void dijkstra(int u, vector<int>&d){
    d[u]=0;
    set<pair<int,int>>s;
    s.insert({u,0});
    while(!s.empty()){
        int p  = s.begin()->first;
        s.erase(s.begin());
        for(auto v : adj[p]){
            int to = v.first;
            int w =  v.second;
            if(w+d[p]<d[to]){
                s.erase({to,d[to]});
                d[to] = w+d[p];
                s.insert({to,d[to]});
            }
        }
    }
}

