void merge(int a[],int l, int m, int r){
    int temp[r-l+1]={0},i=0;
  //  cout<<r<<"; "; FOR(p,l,r+1) cout<<a[p]<<" "; cout<<"\n";
    int j=l,k=m+1;
    while(j<=m && k<=r){
        if(a[j]<=a[k]) temp[i]=a[j],j++;
        else temp[i]=a[k],k++;
        i++;
    }
    while(j<=m) temp[i++]=a[j++];
    while(k<=r) temp[i++]=a[k++];
    
    i=0; j=l;
    while(j<=r) a[j++] = temp[i++];
}

void mer(int a[], int l, int r){
    if(l<r){
        int m = l + (r-l)/2;
        mer(a,l,m);
        mer(a,m+1,r);
        merge(a,l,m,r);
    }
}
